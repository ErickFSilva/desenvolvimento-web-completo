    <h2>Operadores de comparação (condicional)</h2>

    <p>Também conhecidos como operadores relacionais</p>
    <p>Servem para formar as expressões condionais para o comando 'if' entre outros...</p>

    <ul>
        <li>Igual (<span style="color: darkred; font-weight: bold">==</span>): verifica se os valores comparados são <span style="color: darkred; font-weight: bold">iguais</span></li>
        <li>Idêntico (<span style="color: darkred; font-weight: bold">===</span>): verifica se os valores comparados são <span style="color: darkred; font-weight: bold">iguais e do mesmo tipo</span></li>
        <li>Diferente (<span style="color: darkred; font-weight: bold">!=</span>): verifica se os valores comparados são <span style="color: darkred; font-weight: bold">diferentes</span></li>
        <li>Diferente (<span style="color: darkred; font-weight: bold"><></span>): verifica se os valores comparados são <span style="color: darkred; font-weight: bold">diferentes</span></li>
        <li>Não idêntico (<span style="color: darkred; font-weight: bold">!==</span>): Verifica se os valores comparados são <span style="color: darkred; font-weight: bold">diferentes e de tipos diferentes</span></li>
        <li>Menor (<span style="color: darkred; font-weight: bold"><</span>): verifica se o valor da esquerda é <span style="color: darkred; font-weight: bold">menor</span> que o valor da direita</li>
        <li>Meior (<span style="color: darkred; font-weight: bold">></span>): verifica se o valor da esquerda é <span style="color: darkred; font-weight: bold">maior</span> que o valor da direita</li>
        <li>Menor igual (<span style="color: darkred; font-weight: bold"><=</span>): verifica se o valor da esquerda é <span style="color: darkred; font-weight: bold">menor ou igual</span> que o valor da direita</li>
        <li>Maior igual (<span style="color: darkred; font-weight: bold">>=</span>): verifica se o valor da esquerda é <span style="color: darkred; font-weight: bold">maior ou igual</span> que o valor da direita</li>
    </ul>

